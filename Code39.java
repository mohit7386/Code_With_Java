public class Code39 {
    public static void main(String[]args){
        System.out.println("Printing the Sum of 0 to 10 Numbers:-");
        int sum=0;
        for(int i=0;i<=10;i++){
            sum=sum+i;
        }
            System.out.println("The sum of numbers from 0 to 10 is: "+sum);
        
    }
    
}
